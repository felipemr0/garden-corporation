    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
        for(Cliente cliente: sistema.getLfcc()){
            if(cliente.getNumero().equals(jTextField1.getText())){
                for(Produto produto: sistema.getLprod()){
                    if(produto.getIdProd().equals(jTextField2.getText())){
                        sistema.regEnc(new Encomenda(cliente,produto,Integer.parseInt(jTextField3.getText())));
                        if(sistema.getLenc().size()!= 0){
               JOptionPane.showMessageDialog(rootPane,"Encomenda efetuada com sucesso");
               setVisible(false);
                        }
                    }
                }
            }
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane,"Erro");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

